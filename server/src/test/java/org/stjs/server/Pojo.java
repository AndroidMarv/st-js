package org.stjs.server;

public class Pojo {
	private String s;
	private int n;

	public Pojo() {
		//
	}

	public Pojo(String s, int n) {
		this.s = s;
		this.n = n;
	}

	public String getS() {
		return s;
	}

	public void setS(String s) {
		this.s = s;
	}

	public int getN() {
		return n;
	}

	public void setN(int n) {
		this.n = n;
	}

}
